<template>
  <picture :class="classNames.picture">
    <picture-source v-for="(source) in formatSources" :key="source.key" :source="source" />
    <base-image :preload-sources="preloadSources" :class="classNames.image" :title="title" :loading-spinner="loadingSpinner" @load="onLoad" />
  </picture>
</template>

<script>
import BaseImage from '../image';
import LoadingSpinner from '../image/classes/LoadingSpinner';
import PictureSource from './source';
import ImageSourceList from './classes/ImageSourceList';

export default {
  components: {
    PictureSource,
    BaseImage
  },

  props: {
    sources: {
      type: ImageSourceList,
      required: true
    },

    loadingSpinner: {
      type: LoadingSpinner,
      default () {
        return null;
      }
    },

    title: {
      type: String,
      default: null
    }
  },

  data () {
    return {
      meta: {},
      classNames: {}
    };
  },

  fetchKey: 'picture',
  async fetch () {
    this.meta = await this.sources.getMeta(this.$img);
    this.classNames = this.meta.classNames;
  },

  computed: {
    formatSources () {
      return this.sources.getFormats(['jpg']); // 'avif', 'webp',
    },

    preloadSources () {
      return this.sources.getFormats(['avif']);
    }
  },

  methods: {
    onLoad (e) {
      // console.log('LOAD', e);
    }
  }
};
</script>

